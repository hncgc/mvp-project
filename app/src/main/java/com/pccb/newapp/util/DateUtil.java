    /**
     * 以友好的方式显示时间
     *
     * @param sdate
     * @return
     */
    public static String friendly_time(String sdate) {
        Date time = strToDateLong(sdate);
        if (time == null) {
            return "Unknown";
        }
        String ftime = "";
        Calendar cal = Calendar.getInstance();
        // 判断是否是同一天
        String curDate = dateToStr(cal.getTime());
        String paramDate = dateToStr(time);
        if (curDate.equals(paramDate)) {
            int hour = (int) ((cal.getTimeInMillis() - time.getTime()) / 3600000);
            if (hour == 0) {
                int myminute = (int) ((cal.getTimeInMillis() - time.getTime()) / 60000);
                if (myminute == 0) {
                    ftime = "刚才";
                } else {
                    ftime = Math
                            .max((cal.getTimeInMillis() - time.getTime()) / 60000,
                                    1)
                            + "分钟前";
                }
            } else {
                ftime = hour + "小时前";
            }
            return ftime;
        }

        long lt = time.getTime() / 86400000;
        long ct = cal.getTimeInMillis() / 86400000;
        int days = (int) (ct - lt);
        if (days == 0) {
            int hour = (int) ((cal.getTimeInMillis() - time.getTime()) / 3600000);
            if (hour == 0)
                ftime = Math.max(
                        (cal.getTimeInMillis() - time.getTime()) / 60000, 1)
                        + "分钟前";
            else
                ftime = hour + "小时前";
        } else if (days == 1) {
            ftime = "昨天";
        } else if (days == 2) {
            ftime = "前天";
        } else if (days > 2 && days <= 10) {
            ftime = days + "天前";
        } else if (days > 10) {
            ftime = dateToStr(time);
        }
        return ftime;
    }

    /**
     * 字符串日期加年、月、日
     *
     * @param format  ： 日期格式
     * @param StrDate ：字符串日期
     * @param year    ：加年数
     * @param month   ：加月数
     * @param day     ：加日数
     * @return
     */
    public static String GetSysDate(String format, String StrDate, int year,
                                    int month, int day) {
        Calendar cal = Calendar.getInstance();
        SimpleDateFormat sFmt = new SimpleDateFormat(format);
        cal.setTime(sFmt.parse((StrDate), new ParsePosition(0)));

        if (day != 0) {
            cal.add(cal.DATE, day);
        }
        if (month != 0) {
            cal.add(cal.MONTH, month);
        }
        if (year != 0) {
            cal.add(cal.YEAR, year);

        }
        return sFmt.format(cal.getTime());
    }

    /**
     * 获得本月未获利息时间的初始时间段 yyyy-MM-dd
     *
     * @param start_interest_time 项目开始起息时间的时间段
     * @return 最近一个月的利息时间 yyyy-MM-dd
     */
    public static String getThisMothdInterestTime(long start_interest_time) {
        // 获取项目起息的日期，
        Date startdate = new Date(start_interest_time);
        SimpleDateFormat daysdf = new SimpleDateFormat("dd");
        SimpleDateFormat mothsdf = new SimpleDateFormat("MM");
        SimpleDateFormat yearsdf = new SimpleDateFormat("yyyy");
        String daystring = daysdf.format(startdate);
        // 获取今天的日期
        Date nowdate = getNow();
        String nowdaystring = daysdf.format(nowdate);

        String startintersttimestr = "";
        // 比较日期大小（如果大于这是这个月 如果小于这是上个月）
        if (Integer.valueOf(nowdaystring) > Integer.valueOf(daystring)) {
            // 本月
            String years = yearsdf.format(nowdate);
            String moth = mothsdf.format(nowdate);
            startintersttimestr = years + "-" + moth + "-" + daystring;
        } else {
            // 上一月
            String years = yearsdf.format(nowdate);
            String moth = mothsdf.format(nowdate);
            if (Integer.valueOf(moth) == 1) {
                years = (Integer.valueOf(years) - 1) + "";
                moth = 12 + "";
            } else {
                moth = (Integer.valueOf(moth) - 1) + "";
            }
            startintersttimestr = years + "-" + moth + "-" + daystring;
        }
        String dateString = startintersttimestr;
        return dateString;
    }

